public class PrintValues {
    public static void main(String[] args) {

        int integerValue = 42;
        double doubleValue = 3.14159;
        char charValue = 'A';
        String stringValue = "Hello, World!";
        boolean booleanValue = true;

        System.out.print("Integer value: " + integerValue + "\n");
        System.out.print("Double value: " + doubleValue + "\n");
        System.out.print("Character value: " + charValue + "\n");
        System.out.print("String value: " + stringValue + "\n");
        System.out.print("Boolean value: " + booleanValue + "\n");

        System.out.println("Integer value: " + integerValue);
        System.out.println("Double value: " + doubleValue);
        System.out.println("Character value: " + charValue);
        System.out.println("String value: " + stringValue);
        System.out.println("Boolean value: " + booleanValue);
    }
}
